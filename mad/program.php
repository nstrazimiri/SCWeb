            <section>
                <h4>PROGRAM OVERVIEW</h4>
                <p>This advanced diploma program is designed for those who are interested in developing mobile applications
                    for smartphones, tablets, and the web. Students will learn and apply techniques in writing apps for
                    major platforms common in today’s mobile application marketplace. In addition, students will develop a
                    deep understanding of object-oriented programming principles, cross-platform development both web and
                    native, user interface design, database fundamentals, systems development, and project management, as
                    well as being introduced to game development techniques.</p>
                <p>Students will gain knowledge and skills in these areas of study:</p>
                <ul>
                    <li>Analyze and design mobile applications based on user specifications using object orientation.</li>
                    <li>Develop, test, and deploy a variety of native mobile applications for multiple platforms and devices.</li>
                    <li>Develop, test, and deploy a variety of mobile web-based client server-side applications for multiple platforms and devices.</li>
                    <li>Design, model, implement and maintain databases for both web site and mobile applications.</li>
                    <li>Select and apply current and relevant security features for implementation in both enterprise and personal device applications.</li>
                    <li>Design, develop and administer mobile applications by applying User Interface design skills and guidelines.</li>
                    <li>Coordinate or participate as a member of an agile project management team in the development of basic to complex web-based and mobile applications.</li>
                    <li>Analyze, implement and test solutions to performance issues for optimization of mobile website and native applications.</li>
                </ul>
